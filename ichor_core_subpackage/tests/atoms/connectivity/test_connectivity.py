water_path = example_dir / "WATER-3000.xyz"
@pytest.mark.parametrize('timestep', [TimeStep(water_path, i, t) for (i, t) in enumerate(Trajectory(water_path))])
def test_water_3000(timestep: TimeStep):
    np.testing.assert_equal(calculate_connectivity(timestep.atoms), water_connectivity)
    np.testing.assert_equal(timestep.atoms.connectivity, water_connectivity)

